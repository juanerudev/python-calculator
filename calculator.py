number_one = int(input("Ingrese un número: "))
number_two = int(input("Ingrese un número: "))

suma = number_one + number_two
resta = number_one - number_two
multiplicacion = number_one * number_two
division = number_one / number_two

print(f"{number_one} + {number_two} = {suma}")
print(f"{number_one} - {number_two} = {resta}")
print(f"{number_one} * {number_two} = {multiplicacion}")
print(f"{number_one} / {number_two} = {division}")